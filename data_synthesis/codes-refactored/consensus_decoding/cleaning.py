"""Output text cleaning for model continuations and futures."""
from __future__ import annotations

import re

def is_valid_future_text(text: str) -> bool:
    if not text:
        return False
    if re.search(r"[\u3400-\u4dbf\u4e00-\u9fff\uf900-\ufaff]", text):
        return False
    return True